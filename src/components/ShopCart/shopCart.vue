<template>
  <div>
    <div class="shopcart">
      <div class="content">
        <div class="content-left"
             @click="isShowCartList">
          <div class="logo-wrapper">
            <div class="logo"
                 :class="{highlight : totalCount}">
              <i class="iconfont icongouwuche"
                 :class="{highlight : totalCount}"></i>
            </div>
            <div v-show="totalCount"
                 class="num">{{totalCount}}
            </div>
          </div>
          <div class="price"
               :class="{highlight : totalCount}">￥{{totalPrice}}
          </div>
          <div class="desc">另需配送费￥{{shopInfo.deliveryPrice}}元</div>
        </div>
        <div class="content-right">
          <div class="pay"
               :class="payClass">
            {{payText}}
          </div>
        </div>
      </div>
      <transition name="move">
        <div v-show="listShow"
             class="shopcart-list">
          <div class="list-header">
            <h1 class="title">购物车</h1>
            <span class="empty"
                  @click="clearCart">清空</span>
          </div>
          <div class="list-content">
            <ul>
              <li v-for="(cartGood, index) in cartGoods"
                  class="food"
                  :key="index">
                <span class="name">{{cartGood.name}}</span>
                <div class="price"><span>￥{{cartGood.price*cartGood.count}}</span></div>
                <div class="cartcontrol-wrapper">
                  <CartControl :food="cartGood"/>
                </div>
              </li>
            </ul>
          </div>
        </div>
      </transition>
    </div>
    <div v-show="listShow"
         class="list-mask"
         @click="isShowCartList"></div>
  </div>
</template>

<script>
  import {mapGetters, mapState} from 'vuex';
  import CartControl from '../../components/CartControl/cartControl';
  import BScroll from 'better-scroll';
  import {MessageBox} from 'mint-ui';

  export default {
    data() {
      return {
        isShow: false,
        isShowAlt: false,
        alertText: ''
      };
    },
    components: {CartControl},
    computed: {
      ...mapState(['cartGoods', 'shopInfo']),
      ...mapGetters(['totalCount', 'totalPrice']),
      payClass() {
        const {totalCount} = this;
        if (totalCount) {
          return 'enough';
        } else {
          return 'no-enough';
        }
      },
      payText() {
        const {totalPrice} = this;
        if (!totalPrice) {
          return this.shopInfo.minPrice + '元起送';
        } else if (totalPrice < this.shopInfo.minPrice) {
          return '还差' + (this.shopInfo.minPrice - totalPrice) + '元起送';
        } else {
          return '去结算';
        }
      },
      listShow() {
        if (!this.totalCount) {
          this.isShow = false;
          return false;
        }
        return this.isShow;
      }
    },
    methods: {
      isShowCartList() {
        if (this.totalCount) {
          this.isShow = !this.isShow;
          if (this.isShow) {
            //购物车里有food,并且点击了底部让isShow=true的方式打开购物车列表的情况
            //因为BScroll对象必须依赖更新后的数据
            //数据更新后创建BScroll对象,针对类名为包含ui的外层div
            this.$nextTick(() => {
              //把ui的父级div传参给BScroll对象,之后改ul的所有事件
              //都由BScroll分发,而不受ul源生的dom控制.
              //如果以该方式打开购物车,则会创建多个BScroll实例,
              // 点击一个会触发多个BScroll对象的点击事件.
              if (!this.scroll)//第一次才会创建,之后都不会
              {
                this.scroll = new BScroll('.list-content', {//保存起来第一次创建的对象在this(vm对象)
                  click: true,
                  probeType: 1

                });
              } else {
                this.scroll.refresh();//让滚动条刷新一下,即重新统计内容的高度
              }
            });
          }
        }

      },
      clearCart() {
        MessageBox.confirm('确定要清空购物车吗?').then(action => {
          this.$store.dispatch("clearCart")
        })
        ;
      }
    }
  };
</script>
